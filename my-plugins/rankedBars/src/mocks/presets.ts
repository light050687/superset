/** Parse user-supplied custom JSON safely. Returns empty array on any error. */
export function parseCustomMockJson(raw: unknown): RankedRow[] {
  if (typeof raw !== 'string' || raw.trim() === '') {
    return [];
  }
  try {
    const parsed = JSON.parse(raw);
    if (!Array.isArray(parsed)) {
      return [];
    }
    return parsed.filter(
      (r): r is RankedRow =>
        r != null &&
        typeof r === 'object' &&
        typeof (r as RankedRow).id === 'string' &&
        typeof (r as RankedRow).name === 'string' &&
        typeof (r as RankedRow).value === 'number',
    );
  } catch {
    return [];
  }
}
